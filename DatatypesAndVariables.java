package basic;

public class DatatypesAndVariables {

	public static void main(String[] args) {
		 byte a=10;
		 System.out.println("Byte Variable a="+a);
		 
		 short b=20000;
		 System.out.println("Short Variable b="+b);
		 
		 int c=100000;
		 System.out.println("Int Variable c="+c);
		 
		 long d=123456789876543l;
		 System.out.println("Long Variable d="+d);
		 
		 double a1=5.6543218923411111111111111111111111111111111111;
		 System.out.println("double Variable a1="+a1);
		 
		 float a2=3.12345678987654f;
		 System.out.println("float Variable a2="+a2);
		 
		 boolean answer=true;
		 System.out.println("boolean Variable answer="+answer);
		 
		 String emailid="jyo1234@gmail.com";
		 System.out.println("String Variable emailid="+emailid);
		 
		 char gender='F';
		 System.out.println("Char Variable gender="+gender);
		 
	}

}
